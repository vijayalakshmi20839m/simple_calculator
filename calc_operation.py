import math
def myaddition(a,b):
	"""taking two arguments a and b and returns -> a sum of their values  """
	return a+b
def mysubtraction(a,b):
	"""taking two arguments a and b and returns -> a difference of their values  """
	return a-b
def mymultiplication(a,b):
	"""taking two arguments a and b and returns -> a product of their values """
	return a*b
def mydivision(a,b):
	"""taking two arguments a and b and returns -> a division of their values  """
	return a/b
def mysine(a):
	"""taking a as an arguments and  returns -> a sine value  """
	return ((math.sin(a))*3.14)/180
def mycosine(a):
	"""taking a as an arguments and returns -> a sum of their values of same type """
	return (((math.cos(a))*3.14)/180)
def mypowerof(a,b):
	"""taking two arguments a as base value and b as exponent value and returns -> a power value """
	return (math.pow(a,b))
def myroot(a):
	"""taking a as an arguments and returns -> a root value """
	return (math.sqrt(a))
